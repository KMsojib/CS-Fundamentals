#include <stdio.h>

int main() {
    int age = 20;
    float pi = 3.1416;
    char grade = 'A';

    // Printing different data types
    printf("Age = %d\n", age);     // %d for integers
    printf("Pi = %.2f\n", pi);     // %.2f for 2 decimal float
    printf("Grade = %c\n", grade); // %c for character

    return 0;
}
